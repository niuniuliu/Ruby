require 'date'

p Date.new(2001,2,3)  >> 1             #=> #<Date: 2001-03-03 ...> 
p Date.new(2001,1,31) >> 1             #=> #<Date: 2001-02-28 ...>
p Date.new(2001,2,3)  >> -2            #=> #<Date: 2000-12-03 ...>

p DateTime.new(2001,2,3)  >> 1         #=> #<Date: 2001-03-03T00:00:00+00:00 ...> 
p DateTime.new(2001,1,31) >> 1         #=> #<Date: 2001-02-28T00:00:00+00:00 ...>
p DateTime.new(2001,2,3)  >> -2        #=> #<Date: 2000-12-03T00:00:00+00:00 ...>