h1                                                 = { "a" => 100, "b" => 200 }
h2                                                 = { "b" => 254, "c" => 300 }
puts h1.update(h2).inspect                         #=> {"a"=>100, "b"=>254, "c"=>300}

h1                                                 = { "a" => 100, "b" => 200 }
h2                                                 = { "b" => 254, "c" => 300 }
puts h1.update(h2) { |key, v1, v2| v1 }.inspect    #=> {"a"=>100, "b"=>200, "c"=>300}